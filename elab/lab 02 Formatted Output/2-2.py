#String Concatenate Operator : การเชื่อม String

# using + sign in print (only in same datatype)
output = "Hello" + "Python" + "is easy."
print(output) # output : HelloPythonis easy.

output = 34 + 24 + 35 + 170.0
print(output) # output : 263.0

output = str(34) + str(24) + str(35) + str(170.0)
print(output)  # output : 342435170.0

age = 19
output = "I'm " + str(age) + " yrs."
print(output) # output : I'm 19 yrs.